namespace DStarLiteSharp
{
    internal class Ipoint2
    {
        public int x;

        public int y;

        // default constructor
        public Ipoint2()
        {
        }

        // overloaded constructor
        public Ipoint2(int x, int y)
        {
            this.x = x;
            this.y = y;
        }
    }
}